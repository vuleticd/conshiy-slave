class Conshiy_Slave_Block_Adminhtml_System_Config_Form_Button_Validate extends Mage_Adminhtml_Block_System_Config_Form_Field
{
    /*
     * Set template
     */
    protected function _construct()
    {
        parent::_construct();
        $this->setTemplate('conshiy/system/config/button/validate.phtml');
    }
 
    /**
     * Return element html
     *
     * @param  Varien_Data_Form_Element_Abstract $element
     * @return string
     */
    protected function _getElementHtml(Varien_Data_Form_Element_Abstract $element)
    {
        return $this->_toHtml();
    }
 
    /**
     * Return ajax url for button
     *
     * @return string
     */
    public function getAjaxCheckUrl()
    {
        return Mage::helper('adminhtml')->getUrl('conshiy/adminhtml_validate/ajax');
    }
 
    /**
     * Generate button html
     *
     * @return string
     */
    public function getButtonHtml()
    {
        $button = $this->getLayout()->createBlock('adminhtml/widget_button')
            ->setData(array(
            'id'        => 'conshiy_validate_button',
            'label'     => $this->helper('conshiyslave')->__('Validate'),
            'onclick'   => 'javascript:conshiyValidate(); return false;'
        ));
 
        return $button->toHtml();
    }
}